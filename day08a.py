def process(lines):
    total = 0
    for line in lines:
        _, output = line.strip().split(" | ")
        output = [len(digits) for digits in output.split(" ")]
        # 1
        total += output.count(2)
        # 4
        total += output.count(4)
        # 7
        total += output.count(3)
        # 8
        total += output.count(7)
    return total

